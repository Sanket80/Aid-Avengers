class EventData {
  final String state;
  final String city;
  final String emergencyNo;
  final DateTime time;
  final String news;
  final String title;
  final String typeOfDisaster;

  EventData({
    required this.state,
    required this.city,
    required this.emergencyNo,
    required this.time,
    required this.news,
    required this.title,
    required this.typeOfDisaster,
  });
}
